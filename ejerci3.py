#Declarar las variables y asignar numeros
n1 = 10
n2 = 15
n3 = 20

#Calcular el promedio sumando los dos numeros ingresados y dividiendo el resultado por la cantidad de numeros 
promedio = (n1 + n2 + n3) / 3

#Imprimir el mensaje con el resultado del promedio de los tres numeros 

print (f"El promedio de los numeros ingresados es: {promedio}")